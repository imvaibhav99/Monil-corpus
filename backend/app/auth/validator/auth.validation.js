import Joi from 'joi';
import { password } from '../../../helpers/customValidation.js';
import { roles } from '../../../config/roles.js';

// Public signup is restricted to CLIENT/CONTRACTOR. ADMIN/SUPER_ADMIN are
// created via the seed script or by a SUPER_ADMIN through user routes.
const publicSignupRoles = roles.filter((r) => r === 'CLIENT' || r === 'CONTRACTOR');

export const register = {
  body: Joi.object().keys({
    email: Joi.string().required().email(),
    password: Joi.string().required().custom(password),
    name: Joi.string().required(),
    role: Joi.string()
      .valid(...publicSignupRoles)
      .default('CLIENT'),
  }),
};

export const login = {
  body: Joi.object().keys({
    email: Joi.string().required(),
    password: Joi.string().required(),
  }),
};

export const logout = {
  body: Joi.object().keys({
    refreshToken: Joi.string().required(),
  }),
};

export const refreshTokens = {
  body: Joi.object().keys({
    refreshToken: Joi.string().required(),
  }),
};

export const forgotPassword = {
  body: Joi.object().keys({
    email: Joi.string().email().required(),
  }),
};

export const resetPassword = {
  query: Joi.object().keys({
    token: Joi.string().required(),
  }),
  body: Joi.object().keys({
    password: Joi.string().required().custom(password),
  }),
};

export const verifyEmail = {
  query: Joi.object().keys({
    token: Joi.string().required(),
  }),
};
